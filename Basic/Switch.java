package Basic;

public class Switch {
   public static void main(String[] args) {
    int n=2;
    switch (n) {
        case 1:System.out.println("Sam");
            
            break;
        case 2:System.out.println("David");
        break;
        case 3:System.out.println("Rahul");
        break;
    
        default:System.out.println("It's just a beginning");
            break;
    }
   }
}
